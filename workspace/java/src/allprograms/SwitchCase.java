package allprograms;

public class SwitchCase {

	public static void main(String[] args) {
		int i = 7;
		
		switch(i)
		{
		case 0:
			System.out.println("zero");
			break;
		case 1:
			System.out.println("One");
			break;
		case 7:
			System.out.println("Seven");
			break;
		case 9:
			System.out.println("Nine");
			break;
		default:
			System.out.println("None");
		}

	}

}
